# Primeiro, adicionamos a varaivel de numero:
num = float(input('Digite um numero: '))

# Depois adicionamos a varaivel para o numero elevado ao quadrado:
num_quadrado = num**2
print(f'O numero digitado ao quadrado: {num_quadrado}')

# Depois adicionamos uma variavel para raiz quadrada do numero:
num_raiz_2 = num**0.5
print(f'A raiz quadrada do numero digitado: {num_raiz_2:.1f}')

# E por ultimo, adicionamos uma variavel para raiz cubica do numero:
num_raiz_3 = num*1/3
print(f'A raiz cubica do numero digitado: {num_raiz_3:.1f}')